-- ==========================================
-- SCHEMA DEFINITION
-- ==========================================

CREATE TABLE PESSOA (
    CPF CHAR(11) NOT NULL UNIQUE,
    NOME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) UNIQUE,
    TELEFONE VARCHAR(15),
    ENDERECO VARCHAR(200),
    PRIMARY KEY (CPF)
);

CREATE TABLE CLIENTE (
    CPF CHAR(11) NOT NULL,
    DATA_CADASTRO DATE NOT NULL,
    PRIMARY KEY (CPF),
    FOREIGN KEY (CPF) REFERENCES PESSOA(CPF) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE FUNCIONARIO (
    CPF CHAR(11) NOT NULL UNIQUE,
    ESPECIALIDADE VARCHAR(50) NOT NULL,
    PRIMARY KEY (CPF),
    FOREIGN KEY (CPF) REFERENCES PESSOA(CPF) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE ANIMAL (
    ANIMAL_CPF CHAR(11) NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    RACA VARCHAR(50),
    ESPECIE VARCHAR(50),
    SEXO VARCHAR(10),
    PESO DECIMAL(6,2),
    DATA_NASCIMENTO DATE,
    PRIMARY KEY (ANIMAL_CPF, NOME),
    FOREIGN KEY (ANIMAL_CPF) REFERENCES CLIENTE(CPF) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE ANIMAL_ALERGIAS (
    ANIMAL_CPF CHAR(11) NOT NULL,
    ANIMAL_NOME VARCHAR(100) NOT NULL,
    ALERGIA VARCHAR(100) NOT NULL,
    PRIMARY KEY (ANIMAL_CPF, ANIMAL_NOME, ALERGIA),
    FOREIGN KEY (ANIMAL_CPF, ANIMAL_NOME) REFERENCES ANIMAL (ANIMAL_CPF, NOME) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE SERVICO (
    SERVICO_CPF CHAR(11) NOT NULL,
    DATA_HORA TIMESTAMP NOT NULL,
    PRECO DECIMAL(10,2),
    TIPO VARCHAR(50),
    DESCRICAO VARCHAR(200),
    ANIMAL_CPF CHAR(11) NOT NULL,
    ANIMAL_NOME VARCHAR(100) NOT NULL,
    PRIMARY KEY (SERVICO_CPF, DATA_HORA),
	FOREIGN KEY (ANIMAL_CPF, ANIMAL_NOME) REFERENCES ANIMAL (ANIMAL_CPF, NOME) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (SERVICO_CPF) REFERENCES CLIENTE (CPF) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE REALIZA (
    FUNCIONARIO_CPF CHAR(11) NOT NULL,
    SERVICO_CPF CHAR(11) NOT NULL,
    SERVICO_DATA_HORA TIMESTAMP NOT NULL,
    PRIMARY KEY (FUNCIONARIO_CPF, SERVICO_CPF, SERVICO_DATA_HORA),
    FOREIGN KEY (FUNCIONARIO_CPF) REFERENCES FUNCIONARIO(CPF) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (SERVICO_CPF, SERVICO_DATA_HORA) REFERENCES SERVICO(SERVICO_CPF, DATA_HORA) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE COMPRA (
    ID_COMPRA CHAR(13) NOT NULL UNIQUE,
    DATA_HORA TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    MEIO VARCHAR(50),
    PARCELA INT,
    STATUS VARCHAR(50),
    CPF_CLIENTE CHAR(11),
    PRIMARY KEY (ID_COMPRA),
    FOREIGN KEY (CPF_CLIENTE) REFERENCES CLIENTE(CPF) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE FORNECEDOR (
    CNPJ CHAR(14) NOT NULL UNIQUE,
    NOME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) UNIQUE,
    TELEFONE VARCHAR(15),
    CATEGORIA VARCHAR(50),
    PRIMARY KEY (CNPJ)
);

CREATE TABLE PRODUTO (
    ID_PRODUTO CHAR(13) NOT NULL UNIQUE,
    QTDE_MINIMA INT,
    QTDE_ESTOQUE INT,
    DESCRICAO VARCHAR(100),
    CATEGORIA VARCHAR(50),
    PRECO_VENDA DECIMAL(10,2) NOT NULL,
    PRECO_COMPRA DECIMAL(10,2) NOT NULL,
    COD_COMPRA	CHAR(13),
    S_CPF		CHAR(11),
    DATA_HORA	TIMESTAMP,
    FORN_CNPJ	CHAR(14),
    PRIMARY KEY (ID_PRODUTO),
    FOREIGN KEY (COD_COMPRA) REFERENCES COMPRA (ID_COMPRA) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (S_CPF, DATA_HORA) REFERENCES SERVICO (SERVICO_CPF, DATA_HORA) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (FORN_CNPJ) REFERENCES FORNECEDOR(CNPJ) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE  COMPRA_INCLUI (
    SERVICO_CPF CHAR(11),
    SERVICO_DATA_HORA TIMESTAMP,
    ID_COMPRA CHAR(13) NOT NULL,
    ID_PRODUTO CHAR(13) NOT NULL,
    QUANTIDADE INT,
    PRIMARY KEY (ID_COMPRA, ID_PRODUTO),
    FOREIGN KEY (SERVICO_CPF, SERVICO_DATA_HORA) REFERENCES SERVICO(SERVICO_CPF, DATA_HORA) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (ID_COMPRA) REFERENCES COMPRA (ID_COMPRA) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO (ID_PRODUTO) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE  LOTE (
    ID_LOTE CHAR(13) NOT NULL UNIQUE,
    ID_PROD CHAR(13) NOT NULL,
    F_CNPJ CHAR(14) NOT NULL,
    QUANTIDADE INT,
    PRIMARY KEY (ID_LOTE),
    FOREIGN KEY (ID_PROD) REFERENCES PRODUTO (ID_PRODUTO)
    ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (F_CNPJ) REFERENCES FORNECEDOR (CNPJ)
    ON DELETE CASCADE ON UPDATE CASCADE
);

