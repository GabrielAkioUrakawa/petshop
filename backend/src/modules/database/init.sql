-- ==========================================
-- SCHEMA DEFINITION
-- ==========================================

CREATE TABLE IF NOT EXISTS PESSOA (
    CPF CHAR(11) NOT NULL PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) UNIQUE,
    TELEFONE VARCHAR(15),
    ENDERECO VARCHAR(200)
);

CREATE TABLE IF NOT EXISTS CLIENTE (
    CPF CHAR(11) NOT NULL PRIMARY KEY,
    DATA_CADASTRO DATE NOT NULL,
    FOREIGN KEY (CPF) REFERENCES PESSOA(CPF) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS FUNCIONARIO (
    CPF CHAR(11) NOT NULL PRIMARY KEY,
    ESPECIALIDADE VARCHAR(50) NOT NULL,
    FOREIGN KEY (CPF) REFERENCES PESSOA(CPF) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS ANIMAL (
    NOME VARCHAR(100) NOT NULL,
    DONO_CPF CHAR(11) NOT NULL,
    RACA VARCHAR(50),
    ESPECIE VARCHAR(50),
    SEXO VARCHAR(10),
    PESO DECIMAL(6,2),
    DATA_NASCIMENTO DATE,
    PRIMARY KEY (NOME, DONO_CPF),
    FOREIGN KEY (DONO_CPF) REFERENCES CLIENTE(CPF) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS FORNECEDOR (
    CNPJ CHAR(14) NOT NULL PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) UNIQUE,
    TELEFONE VARCHAR(15),
    CATEGORIA VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS COMPRA (
    ID_COMPRA SERIAL PRIMARY KEY,
    DATA_HORA TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    MEIO VARCHAR(50),
    PARCELA INT,
    STATUS VARCHAR(50),
    CPF_CLIENTE CHAR(11) NOT NULL,
    FOREIGN KEY (CPF_CLIENTE) REFERENCES CLIENTE(CPF)
);

CREATE TABLE IF NOT EXISTS PRODUTO (
    ID_PRODUTO SERIAL PRIMARY KEY,
    DESCRICAO VARCHAR(100) NOT NULL,
    CATEGORIA VARCHAR(50),
    PRECO_VENDA DECIMAL(10,2) NOT NULL,
    QTDE_ESTOQUE INT NOT NULL DEFAULT 0,
    QTDE_MINIMA INT NOT NULL DEFAULT 10,
    FORN_CNPJ CHAR(14),
    FOREIGN KEY (FORN_CNPJ) REFERENCES FORNECEDOR(CNPJ)
);

CREATE TABLE IF NOT EXISTS SERVICO (
    SERVICO_CPF CHAR(11) NOT NULL,
    DATA_HORA TIMESTAMP NOT NULL,
    PRECO DECIMAL(10,2),
    TIPO VARCHAR(50),
    DESCRICAO VARCHAR(200),
    FUNCIONARIO_CPF CHAR(11) NOT NULL,
    ANIMAL_NOME VARCHAR(100) NOT NULL,
    ANIMAL_CPF CHAR(11) NOT NULL,
    PRIMARY KEY (SERVICO_CPF, DATA_HORA),
    FOREIGN KEY (SERVICO_CPF) REFERENCES CLIENTE(CPF),
    FOREIGN KEY (FUNCIONARIO_CPF) REFERENCES FUNCIONARIO(CPF),
    FOREIGN KEY (ANIMAL_NOME, ANIMAL_CPF) REFERENCES ANIMAL(NOME, DONO_CPF)
);

CREATE TABLE IF NOT EXISTS COMPRA_INCLUI (
    ID_COMPRA INT NOT NULL,
    ID_PRODUTO INT NOT NULL,
    QUANTIDADE INT NOT NULL,
    PRECO_UNITARIO DECIMAL(10,2) NOT NULL,
    SERVICO_CPF CHAR(11),
    SERVICO_DATA_HORA TIMESTAMP,
    PRIMARY KEY (ID_COMPRA, ID_PRODUTO),
    FOREIGN KEY (ID_COMPRA) REFERENCES COMPRA(ID_COMPRA) ON DELETE CASCADE,
    FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO(ID_PRODUTO) ON DELETE CASCADE,
    FOREIGN KEY (SERVICO_CPF, SERVICO_DATA_HORA) REFERENCES SERVICO(SERVICO_CPF, DATA_HORA) ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS LOTE (
    ID_LOTE SERIAL PRIMARY KEY,
    DATA_VALIDADE DATE NOT NULL,
    QUANTIDADE INT NOT NULL,
    ID_COMPRA INT NOT NULL,
    ID_PRODUTO INT NOT NULL,
    FOREIGN KEY (ID_COMPRA) REFERENCES COMPRA(ID_COMPRA),
    FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO(ID_PRODUTO)
);

