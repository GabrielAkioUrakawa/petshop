CREATE TABLE PESSOA
    (  CPF            CHAR(11)        NOT NULL UNIQUE,
       NOME           VARCHAR(30)     NOT NULL,
       EMAIL          VARCHAR(30) UNIQUE,
       TELEFONE       VARCHAR(15),
       ENDERECO       VARCHAR(50), 
       PRIMARY KEY (CPF)
    );


CREATE TABLE CLIENTE
    (  CPF         CHAR(11)    NOT NULL UNIQUE,
       DATA_CADASTRO       DATE        NOT NULL,
       PRIMARY KEY (CPF),
       FOREIGN KEY (CPF) REFERENCES PESSOA (CPF)
    );


CREATE TABLE FUNCIONARIO
    (  CPF     CHAR(11)        NOT NULL UNIQUE,
       ESPECIALIDADE       VARCHAR(15)     NOT NULL,
       PRIMARY KEY (CPF),
       FOREIGN KEY (CPF) REFERENCES PESSOA (CPF)
    );


CREATE TABLE ANIMAL
    (  CLIENT_CPF         CHAR(11)        NOT NULL UNIQUE,
       NOME               VARCHAR(30)     NOT NULL,
       RACA               VARCHAR(15),
       ESPECIE            VARCHAR(15),
       SEXO               VARCHAR(10),
       PESO               DECIMAL(4,2),
       DATA_NASCIMENTO    DATE,
       PRIMARY KEY (CLIENT_CPF),
       FOREIGN KEY (CLIENT_CPF) REFERENCES CLIENTE (CPF)
    );


CREATE TABLE SERVICO
    (  
       DATA_HORA           DATE            NOT NULL,
       PRECO               DECIMAL(5,2),
       TIPO                VARCHAR(10),
       DESCRICAO           VARCHAR(15),
       CLIENTE_CPF         CHAR(11)        NOT NULL,
       FUNCIONARIO_CPF     CHAR(11)        NOT NULL,
       ANIMAL_CPF          CHAR(11)        NOT NULL,
       PRIMARY KEY (CLIENTE_CPF, DATA_HORA),
       FOREIGN KEY (CLIENTE_CPF) REFERENCES CLIENTE (CPF),
       FOREIGN KEY (FUNCIONARIO_CPF) REFERENCES FUNCIONARIO (CPF),
       FOREIGN KEY (ANIMAL_CPF) REFERENCES ANIMAL (CLIENT_CPF)
    );


CREATE TABLE ANIMAL_ALERGIAS
    (  CLIENT_CPF         CHAR(11)        NOT NULL,
       ALERGIA            VARCHAR(15)     NOT NULL,
       PRIMARY KEY (CLIENT_CPF, ALERGIA),
       FOREIGN KEY (CLIENT_CPF) REFERENCES CLIENTE (CPF)
    );


CREATE TABLE COMPRA
    (  ID_COMPRA          CHAR(13)        NOT NULL UNIQUE,
       DATA_HORA          DATE,
       MEIO               VARCHAR(15),
       PARCELA            INT,
       STATUS             VARCHAR(15),
       CPF_CLIENTE        CHAR(11)        NOT NULL,
       PRIMARY KEY (ID_COMPRA),
       FOREIGN KEY (CPF_CLIENTE) REFERENCES CLIENTE (CPF)
    );


CREATE TABLE FORNECEDOR
    (  CNPJ               CHAR(14)        NOT NULL UNIQUE,
       NOME               VARCHAR(30)     NOT NULL,
       EMAIL              VARCHAR(30) UNIQUE,
       TELEFONE           VARCHAR(15),
       CATEGORIA          VARCHAR(15),
       PRIMARY KEY (CNPJ)
    );


CREATE TABLE PRODUTO
    (  ID_PRODUTO         CHAR(13)        NOT NULL UNIQUE,
       QTDE_MINIMA        INT,
       QTDE_ESTOQUE       INT,
       DESCRICAO          VARCHAR(50),
       CATEGORIA          VARCHAR(15),
       PRECO_VENDA        DECIMAL(5,2),
       PRECO_COMPRA       DECIMAL(5,2),
       COD_COMPRA         CHAR(13)        NOT NULL,
       FORN_CNPJ          CHAR(14)        NOT NULL,
       PRIMARY KEY (ID_PRODUTO),
       FOREIGN KEY (COD_COMPRA) REFERENCES COMPRA (ID_COMPRA),
       FOREIGN KEY (FORN_CNPJ) REFERENCES FORNECEDOR (CNPJ)
    );



CREATE TABLE SERVICO_PRODUTO
    (  SERVICO_CPF        CHAR(11)        NOT NULL,
       DATA_HORA          DATE            NOT NULL,
       ID_PRODUTO         CHAR(13)        NOT NULL,
       PRIMARY KEY (SERVICO_CPF, DATA_HORA, ID_PRODUTO),
       FOREIGN KEY (SERVICO_CPF, DATA_HORA) REFERENCES SERVICO (CLIENTE_CPF, DATA_HORA),
       FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO (ID_PRODUTO)
    );


CREATE TABLE COMPRA_INCLUI
    (  SERVICO_CPF        CHAR(11)        NOT NULL,
       SERVICO_DATA_HORA  DATE            NOT NULL,
       ID_COMPRA          CHAR(13)        NOT NULL,
       ID_PRODUTO         CHAR(13)        NOT NULL,
       QUANTIDADE         INT,
       PRIMARY KEY (SERVICO_CPF, SERVICO_DATA_HORA, ID_COMPRA, ID_PRODUTO),
       FOREIGN KEY (SERVICO_CPF, SERVICO_DATA_HORA) REFERENCES SERVICO (CLIENTE_CPF, DATA_HORA),
       FOREIGN KEY (ID_COMPRA) REFERENCES COMPRA (ID_COMPRA),
       FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO (ID_PRODUTO)
    );


CREATE TABLE LOTE
    (  ID_LOTE            CHAR(13)        NOT NULL UNIQUE,
       F_CNPJ             CHAR(14)        NOT NULL,
       PRIMARY KEY (ID_LOTE),
       FOREIGN KEY (F_CNPJ) REFERENCES FORNECEDOR (CNPJ)
    );


CREATE TABLE LOTE_INCLUI
    (  ID_PRODUTO         CHAR(13)        NOT NULL,
       ID_LOTE            CHAR(13)        NOT NULL,
       QUANTIDADE         INT,
       PRIMARY KEY (ID_PRODUTO, ID_LOTE),
       FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTO (ID_PRODUTO),
       FOREIGN KEY (ID_LOTE) REFERENCES LOTE (ID_LOTE)
    );


